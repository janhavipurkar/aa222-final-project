for _, r in profitable.iterrows():
    pred = gp_predict(r['a'], r['e'], r['i'], r['w'])
    actual = r['direct_minus_mining_usd']
    print(f"  {str(r['full_name']).strip():30s} "
          f"{actual/1e6:>8.1f}M  {pred/1e6:>8.1f}M  "
          f"{(pred-actual)/1e6:>+8.1f}M")