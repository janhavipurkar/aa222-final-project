import pandas as pd
import numpy as np
import pickle

df_train = pd.read_csv('training_set_1000_ntp.csv')

# Load your GP
with open('gp_economics.pkl', 'rb') as f:
    b = pickle.load(f)
gp, sx, sy = b['gp'], b['scaler_X'], b['scaler_y']

def gp_predict(a, e, i, w):
    w_r = np.deg2rad(w)
    xs = sx.transform([[a, e, i, np.sin(w_r), np.cos(w_r)]])
    s = sy.inverse_transform(gp.predict(xs).reshape(-1,1))[0,0]
    return s

# GP vs actual for ALL 32 profitable training points
print("GP prediction vs ground truth — 32 profitable asteroids:")
print(f"{'Name':30s} {'Actual':>10} {'GP pred':>10} {'error':>10}")
